function similarity(base, candidate) {
  const sharedTags = candidate.tags.filter((tag) => base.tags.includes(tag)).length;
  const ratingDistance = dimensions.reduce((sum, [key]) => sum + Math.abs(base.ratings[key] - candidate.ratings[key]), 0);
  const genreBoost = base.genre === candidate.genre ? 2 : 0;
  const eraBoost = Math.abs(base.year - candidate.year) <= 3 ? 0.8 : 0;
  return sharedTags * 2.4 + genreBoost + eraBoost - ratingDistance * 0.28;
}

function renderRecommendations(baseAlbum) {
  const matches = albums
    .filter((album) => album.id !== baseAlbum.id)
    .map((album) => ({ ...album, matchScore: similarity(baseAlbum, album) }))
    .sort((a, b) => b.matchScore - a.matchScore)
    .slice(0, 3);

  recommendations.innerHTML = matches
    .map((album) => {
      const shared = album.tags.filter((tag) => baseAlbum.tags.includes(tag));
      const reason = shared.length ? `共同标签：${shared.join(" / ")}` : `相近情绪：${album.mood}`;
      return `
        <article class="recommend-card">
          <div class="cover-art">${coverMarkup(album)}</div>
          <div>
            <div class="match">${Math.min(98, Math.max(72, Math.round(88 + album.matchScore * 2)))}% Match</div>
            <h3 class="album-title">${album.title}</h3>
            <div class="album-artist">${album.artist} · ${album.year}</div>
            <p class="album-note">${reason}</p>
          </div>
        </article>
      `;
    })
    .join("");
  hydrateCovers(recommendations);
  initMagneticCards();
}

function shuffle(list) {
  return [...list].sort(() => Math.random() - 0.5);
}

