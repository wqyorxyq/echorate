function normalizeAlbumName(value) {
  return value
    .toLowerCase()
    .replace(/[’']/g, "")
    .replace(/\([^)]*\)|\[[^\]]*\]/g, "")
    .replace(/deluxe|expanded|anniversary|remaster(?:ed)?|edition|explicit|clean|bonus|complete/g, "")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function formalAlbumScore(album, item) {
  const itemTitle = item.collectionName || "";
  const normalizedTarget = normalizeAlbumName(album.title);
  const normalizedResult = normalizeAlbumName(itemTitle);
  const artistToken = normalizeAlbumName(album.artist).split(" ")[0];
  const itemArtist = normalizeAlbumName(item.artistName || "");
  const reject = /single|remix|karaoke|tribute|commentary|instrumental|sped up|slowed|cover/i.test(itemTitle);
  let score = 0;

  if (normalizedResult === normalizedTarget) score += 90;
  if (normalizedResult.includes(normalizedTarget) || normalizedTarget.includes(normalizedResult)) score += 30;
  if (itemArtist.includes(artistToken) || album.artist === "Various Artists") score += 25;
  if ((item.trackCount || 0) >= 7) score += 12;
  if (reject) score -= 80;
  if (/deluxe|expanded|anniversary|remaster|edition|complete/i.test(itemTitle)) score -= 8;
  return score;
}

function pickFormalItunesAlbum(album, results = []) {
  const candidates = results
    .filter((item) => item.collectionType === "Album" && item.artworkUrl100)
    .map((item) => ({ item, score: formalAlbumScore(album, item) }))
    .filter(({ score }) => score > 25)
    .sort((a, b) => b.score - a.score || new Date(a.item.releaseDate) - new Date(b.item.releaseDate));
  const bestScore = candidates[0]?.score ?? -999;
  return candidates.filter(({ score }) => score >= bestScore - 8).sort((a, b) => new Date(a.item.releaseDate) - new Date(b.item.releaseDate))[0]?.item;
}

async function fetchItunesCover(album) {
  const query = encodeURIComponent(`${album.artist} ${album.title}`);
  const response = await fetch(`https://itunes.apple.com/search?term=${query}&entity=album&limit=25&country=US`);
  const data = await response.json();
  const best = pickFormalItunesAlbum(album, data.results);
  return best?.artworkUrl100?.replace("100x100bb", "600x600bb") ?? "";
}

async function fetchMusicBrainzCover(album) {
  const query = encodeURIComponent(`releasegroup:"${album.title}" AND artist:"${album.artist}"`);
  const url = `https://musicbrainz.org/ws/2/release-group/?query=${query}&type=album&fmt=json&limit=5`;
  const response = await fetch(url, { headers: { "User-Agent": "EchoRate/1.0" } });
  const data = await response.json();
  const groups = data["release-groups"] || [];
  const normalizedTarget = normalizeAlbumName(album.title);
  const best = groups
    .filter((group) => group["primary-type"] === "Album")
    .map((group) => {
      const normalizedResult = normalizeAlbumName(group.title || "");
      const exact = normalizedResult === normalizedTarget ? 60 : 0;
      const official = (group.releases || []).some((release) => release.status === "Official") ? 20 : 0;
      return { group, score: (group.score || 0) + exact + official };
    })
    .sort((a, b) => b.score - a.score || new Date(a.group["first-release-date"] || "9999") - new Date(b.group["first-release-date"] || "9999"))[0]?.group;
  return best?.id ? `https://coverartarchive.org/release-group/${best.id}/front-500` : "";
}

async function fetchCover(album) {
  if (album.cover || coverRequests.has(album.id)) return;
  coverRequests.add(album.id);
  try {
    const cover = (await fetchItunesCover(album)) || (await fetchMusicBrainzCover(album));
    if (cover) {
      album.cover = cover;
      document.querySelectorAll(`[data-cover-id="${album.id}"]`).forEach((node) => {
        node.outerHTML = `<img src="${album.cover}" alt="${album.title} 专辑封面" loading="lazy" data-cover-id="${album.id}" />`;
      });
    }
  } catch {
    coverRequests.delete(album.id);
  }
}

function hydrateCovers(scope = document) {
  scope.querySelectorAll("[data-cover-id]").forEach((node) => {
    const album = albums.find((item) => item.id === node.dataset.coverId);
    if (album) fetchCover(album);
  });
}

