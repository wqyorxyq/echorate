function initKineticText() {
  document.querySelectorAll(".kinetic-title, .kinetic-type").forEach((element) => {
    if (element.dataset.kineticReady === "true") return;
    element.dataset.kineticReady = "true";
    const text = element.dataset.text || element.textContent.trim();
    element.setAttribute("aria-label", text);
    element.innerHTML = Array.from(text)
      .map((char, index) => {
        const safeChar = char === " " ? "&nbsp;" : char;
        const className = char === " " ? "kinetic-char kinetic-space" : "kinetic-char";
        return `<span class="${className}" style="--char-index:${index}" aria-hidden="true">${safeChar}</span>`;
      })
      .join("");
  });
}

function initScrambleText() {
  const alphabet = "01<>/{}[]ECHORATE";
  const targets = document.querySelectorAll(".nav a, .primary-action, .secondary-action, .chip, .top10-add, .skip-button");
  targets.forEach((element) => {
    if (element.dataset.scrambleReady === "true") return;
    element.dataset.scrambleReady = "true";
    const original = element.textContent.trim();
    if (!original) return;
    const width = element.getBoundingClientRect().width;
    if (width) element.style.setProperty("--stable-width", `${Math.ceil(width)}px`);
    element.dataset.originalText = original;
    element.addEventListener("pointerenter", () => {
      if (element.dataset.scrambling === "true") return;
      element.dataset.scrambling = "true";
      let frame = 0;
      const total = 13;
      const tick = () => {
        const progress = frame / total;
        element.textContent = Array.from(original)
          .map((char, index) => {
            if (char === " ") return " ";
            if (index / original.length < progress) return char;
            return alphabet[Math.floor(Math.random() * alphabet.length)];
          })
          .join("");
        frame += 1;
        if (frame <= total) {
          window.setTimeout(tick, 24);
        } else {
          element.textContent = original;
          element.dataset.scrambling = "false";
        }
      };
      tick();
    });
  });
}

function initMagneticCards() {
  const cards = document.querySelectorAll(".album-card, .recommend-card, .test-panel, .result-panel, .persona-card, .top10-item");
  cards.forEach((card) => {
    if (card.dataset.magneticReady === "true") return;
    card.dataset.magneticReady = "true";
    let frame = 0;
    let latest = null;
    card.addEventListener("pointermove", (event) => {
      latest = event;
      if (frame) return;
      frame = requestAnimationFrame(() => {
        const rect = card.getBoundingClientRect();
        const x = latest.clientX - rect.left;
        const y = latest.clientY - rect.top;
        const px = x / rect.width;
        const py = y / rect.height;
        card.style.setProperty("--spot-x", `${px * 100}%`);
        card.style.setProperty("--spot-y", `${py * 100}%`);
        card.style.setProperty("--tilt-x", `${(py - 0.5) * -5}deg`);
        card.style.setProperty("--tilt-y", `${(px - 0.5) * 5}deg`);
        frame = 0;
      });
    });
    card.addEventListener("pointerleave", () => {
      card.style.setProperty("--tilt-x", "0deg");
      card.style.setProperty("--tilt-y", "0deg");
    });
  });
}

function initBackgroundInteraction() {
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (reduceMotion) return;

  let frame = 0;
  let x = window.innerWidth * 0.5;
  let y = window.innerHeight * 0.24;

  window.addEventListener("pointermove", (event) => {
    x = event.clientX;
    y = event.clientY;
    if (frame) return;
    frame = requestAnimationFrame(() => {
      document.body.style.setProperty("--cursor-x", `${x}px`);
      document.body.style.setProperty("--cursor-y", `${y}px`);
      frame = 0;
    });
  });
}

