function slugify(value) {
  return value
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function initials(title) {
  return title
    .replace(/[^\w\s]/g, " ")
    .split(/\s+/)
    .filter(Boolean)
    .slice(0, 2)
    .map((word) => word[0])
    .join("")
    .toUpperCase();
}

function inferGenre(artist) {
  const match = genreMap.find(([, artists]) => artists.some((name) => artist.includes(name)));
  return match?.[2] ?? "pop";
}

function inferTags(title, artist, genre) {
  const lower = `${title} ${artist}`.toLowerCase();
  const base = [genre, "grammy-nominated"];
  if (lower.includes("lana") || lower.includes("boygenius") || lower.includes("lorde")) base.push("alt-pop", "lyrical");
  if (lower.includes("kendrick") || lower.includes("jay-z") || lower.includes("drake")) base.push("hip-hop", "narrative");
  if (lower.includes("beyonce") || lower.includes("dua") || lower.includes("charli")) base.push("dance-pop", "club");
  if (lower.includes("billie") || lower.includes("sza") || lower.includes("h.e.r")) base.push("r&b-pop", "intimate");
  if (lower.includes("taylor") || lower.includes("adele") || lower.includes("sabrina")) base.push("songwriting", "radio pop");
  return [...new Set(base)];
}

