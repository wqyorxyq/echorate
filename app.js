function profileFromRatings(ratings) {
  const profile = {};
  ratings.forEach(({ album, score }) => {
    album.tags.forEach((tag) => {
      profile[tag] = (profile[tag] ?? 0) + score;
    });
    profile[album.genre] = (profile[album.genre] ?? 0) + score * 1.2;
  });
  return profile;
}

function recommendationFromProfile(ratings, limit = 8) {
  const ratedIds = ratings.map((item) => item.album.id);
  const profile = profileFromRatings(ratings);
  return albums
    .filter((album) => !ratedIds.includes(album.id))
    .map((album) => {
      const fit = album.tags.reduce((sum, tag) => sum + (profile[tag] ?? 0), 0) + (profile[album.genre] ?? 0);
      return { ...album, fit };
    })
    .sort((a, b) => b.fit - a.fit || b.score - a.score)
    .slice(0, limit);
}

